szam_1 = int(input("Kérem az első számot: "))
szam_2 = int(input("Kérem a második számot: "))

össz = szam_1 + szam_2
kiv = szam_1 - szam_2
szor = szam_1 * szam_2
hány = szam_1 / szam_2

print(f"eredmények: \n\n"
      f"A két szám összege: {össz}\n\n"
      f"A két szám kivonása: {kiv}\n\n"
      f"A két szám szorzata: {szor}\n\n"
      f"A két szám hányadosa: {hány} \n\n"
      f"vége")


